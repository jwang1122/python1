# loop on string
s = "hello world!"
for i in s: # loop for each character
    print(i, end=' ')
print()

# loop on tuple
t=(1,3,5,7,9)
for s in t: # s is the variable for each element in the tuple, you can name it whatever
    print(s, end=' ')
print()

